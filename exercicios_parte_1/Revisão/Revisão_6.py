# # Exercício 6 – Crie um algoritmo em Python que seja possível de calcular a média entre 5 valores informados pelo usuário, mostrar.

nmr1 = int(input("insira um valor para o calculo da media: "))
nmr2 = int(input("insira outra valor para o calculo da media: ")) 
nmr3 = int(input("insira outra valor para o calculo da media: ")) 
nmr4 = int(input("insira outra valor para o calculo da media: ")) 
nmr5 = int(input("insira outra valor para o calculo da media: ")) 

media = (nmr1 + nmr2 + nmr3 + nmr4 + nmr5) / 5

print(f"A media desses valores é: {media}")