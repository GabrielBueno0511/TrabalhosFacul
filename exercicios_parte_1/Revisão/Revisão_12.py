# Exercício 12 – Faça um algoritmo que mostre todos os número de 0 a 100 e para cada um, se é par, ímpar e também se é primo.

i = 0
while (i < 100):
        i = i + 1
        if (i % 2 == 0):
                print("numero par: ", i )

        else:
                print("numero impar: ", i)
        


